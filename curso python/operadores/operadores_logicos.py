#AND
#para que el operador AND devuelva True, ambos valores deben ser True
resultado = True & True #delvuelve true
resultado2 = True & False #delvuelve false
resultado3 = False & False #delvuelve false
resultado4 = False & True #delvuelve false


#OR
#el operador OR devolverá True si al menos uno de los valores es True y false solo si los dos valores son false
resultado5 = True | True #delvuelve true
resultado6 = True | False #delvuelve true
resultado7 = False | True #delvuelve true
resultado8 = False | False #delvuelve false


#NOT
#el operador NOT invierte el valor de la expresión
resultado9 = not True #delvuelve false
resultado10 = not False #delvuelve true
print(resultado9)